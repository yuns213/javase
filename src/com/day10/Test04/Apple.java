package com.day10.Test04;

public class Apple {

    private double Size;
    private String Color;

    public Apple() {
    }

    public Apple(double size, String color) {
        Size = size;
        Color = color;
    }


    public double getSize() {
        return Size;
    }

    public void setSize(double size) {
        Size = size;
    }

    public String getColor() {
        return Color;
    }

    public void setColor(String color) {
        Color = color;
    }



}
